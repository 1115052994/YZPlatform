    @Override
    public void onInitNaviSuccess() {
        //这是步行和骑行调用的
//        mAMapNavi.calculateRideRoute(new NaviLatLng(39.92, 116.43), new NaviLatLng(38.92, 116.53));

        //这是驾驶时调用的
        /**
         * 方法:
         *   int strategy=mAMapNavi.strategyConvert(congestion, avoidhightspeed, cost, hightspeed, multipleroute);
         * 参数:
         * @congestion 躲避拥堵
         * @avoidhightspeed 不走高速
         * @cost 避免收费
         * @hightspeed 高速优先
         * @multipleroute 多路径
         *
         * 说明:
         *      以上参数都是boolean类型，其中multipleroute参数表示是否多条路线，如果为true则此策略会算出多条路线。
         * 注意:
         *      不走高速与高速优先不能同时为true
         *      高速优先与避免收费不能同时为true
         */
        int strategy=0;
        try {
            strategy = mAMapNavi.strategyConvert(true, false, false, false, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
//        start.add(new NaviLatLng(39.92, 116.43));
        end.add(new NaviLatLng(38.92, 116.53));

        //这是from：起点坐标；
        //to：终点坐标；
        //wayPoints：途经点坐标；
        //strategy：路径的计算策略；  第四个参数可以这么写PathPlanningStrategy.DRIVING_MULTIPLE_ROUTES_DEFAULT
        boolean b = mAMapNavi.calculateDriveRoute(end, null,strategy);
        if(b==false){
            Toast.makeText(this, "规划路线失败", Toast.LENGTH_SHORT).show();
        }

    }

